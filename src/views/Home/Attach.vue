<template>
  <div style="nav-constructor">
    <v-navigation-drawer
      width="353"
      permanent
      class="pt-5 px-5 nav-constructor_item"
    >
      <div class="header d-flex justify-space-between">
        <div class="text-h6 d-inline-block">Сотрудники</div>
      </div>
      <v-text-field
        outlined
        v-model="inputEmployies"
        label="Поиск"
        prepend-inner-icon="mdi-magnify"
      ></v-text-field>
      <v-list-item
        draggable
        @dragstart="dragStart($event, employe)"
        v-for="employe in employes"
        :key="employe.value"
      >
        <v-list-item-content>
          <v-list-item-title>{{ employe.user.name }}</v-list-item-title>
        </v-list-item-content>
      </v-list-item>
    </v-navigation-drawer>
    <v-divider></v-divider>
    <v-navigation-drawer
      width="353"
      permanent
      class="pt-5 px-5 nav-constructor_item"
    >
      <div class="header d-flex justify-space-between">
        <div class="text-h6 d-inline-block">Роли</div>
      </div>
      <v-text-field
        outlined
        label="Поиск"
        v-model="inputRoles"
        prepend-inner-icon="mdi-magnify"
      ></v-text-field>
      <v-list-item
        v-for="role in roles"
        :key="role.value"
        draggable
        @dragstart="dragStart($event, role)"
      >
        <v-list-item-content>
          <v-list-item-title>{{ role.name }}</v-list-item-title>
        </v-list-item-content>
      </v-list-item>
    </v-navigation-drawer>
    <v-divider></v-divider>
  </div>
</template>

<script lang="ts">
import treeMixin from "@/mixins/treeMixin";
import { employees } from "@/store/dump";
import { IRole } from "@/store/interfaces";
import { SET_EMPLOYIES, SET_ROLES } from "@/store/mutation-types";
import Vue from "vue";

export default Vue.extend({
  data() {
    return {
      inputRoles: null,
      inputEmployies: null,
      roles: [
        {
          name: "Роль 78",
          id: 0,
        },
        {
          name: "Роль 1",
          id: 1,
        },
        {
          name: "Роль 1",
          id: 2,
        },
        {
          name: "Роль 1",
          id: 3,
        },
      ] as IRole[],
    };
  },
  created() {
    this.$store.dispatch(SET_EMPLOYIES, employees);
    this.$store.dispatch(SET_ROLES, this.roles);
  },
  computed: {
    roles() {
      return this.$store.getters.GET_ATTACH_ITEMS(this.inputRoles, "roles");
    },
    employes() {
      return this.$store.getters.GET_ATTACH_ITEMS(
        this.inputEmployies,
        "employes"
      );
    },
  },
  mixins: [treeMixin],
});
</script>

<style lang="scss" scoped>
.nav-constructor {
  height: 100%;
  .nav-constructor_item {
    height: 50%;
  }
}
</style>
